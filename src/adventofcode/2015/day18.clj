(defn print-grid [{:keys [h w grid]}]
  (println "--------------------------------------------------------------------------------")
  (doseq [y (range 0 (inc h))]
    (doseq [x (range 0 (inc w))]
      (print (if  (grid [y x]) \# \.)))
    (println)
    )
  (println "--------------------------------------------------------------------------------")
  )


