(defn parse [input]
  (let [grid
        (->>
         input
         (str/split-lines)
         (map-indexed (fn [ y line]
                        (map-indexed (fn [ x ch]
                                       [ [y x] (= ch \#) ])
                                     line
                                     )))
         (apply concat)
         (into {}))]
    {:h (apply max (map first (keys grid)))
     :w (apply max (map second (keys grid)))
     :grid grid}

    ))


(defn step [{:keys [h w grid] :as state}]
  (assoc state :grid
         (reduce-kv
          (fn [acc yx on]
            (let [on-neighbours (count (filter grid (neighbour-coords yx)))]
              (assoc acc yx
                     (case on
                       true (case on-neighbours
                              (2 3) true
                              false)
                       false (case on-neighbours
                               3 true
                               false)))))

          grid
          grid)))


(defn corner? [h w yx]
  ( yx))

(defn set-corners [{:keys [h w grid]}]
  {:h h
   :w w
   :grid
   (reduce
    (fn [g corner]
      (assoc g corner true))
    grid
    [[0 0]
     [h 0]
     [0 w]
     [h w]])})

